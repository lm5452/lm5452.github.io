// 1. 定义变量
var bar = document.querySelector('.progress-bar'); // 进度条填充
var text = document.querySelector('.loading-text'); // 百分比文字
var loader = document.getElementById('zyyo-loading'); // 整个加载层
var width = 0;
var timer = null;

// 2. 启动一个“假”进度，防止用户觉得卡死（先跑到 90%）
timer = setInterval(function() {
    if (width >= 90) {
        clearInterval(timer); // 到了90%就停住，等待图片加载
    } else {
        width += Math.floor(Math.random() * 5) + 1; // 随机增加速度，显得自然
        if(width > 90) width = 90; // 限制在90
        bar.style.width = width + '%';
        text.innerHTML = width + '%';
    }
}, 100);

// 3. 监听“所有资源”（包括图片）加载完成
window.addEventListener('load', function() {
    // 图片加载完了，直接把进度条补满到 100%
    width = 100;
    bar.style.width = '100%';
    text.innerHTML = '100%';

    // 稍微停顿一下，让用户看到 100%
    setTimeout(function() {
        // 淡出效果
        loader.style.opacity = '0';
        
        // 彻底移除加载层（让页面可以点击）
        setTimeout(function(){
            loader.style.display = 'none';
        }, 500);
    }, 300);
});
